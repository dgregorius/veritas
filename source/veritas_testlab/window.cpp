#include "window.h"


//--------------------------------------------------------------------------------------------------
// VsWindow
//--------------------------------------------------------------------------------------------------
VsWindow::VsWindow( const char* Name, bool Visible )
	: mName( Name )
	, mVisible( Visible )
	{

	}


//--------------------------------------------------------------------------------------------------
const char* VsWindow::GetName() const
	{
	return mName;
	}


//--------------------------------------------------------------------------------------------------
bool VsWindow::IsVisible() const
	{
	return mVisible;
	}


//--------------------------------------------------------------------------------------------------
void VsWindow::SetVisible( bool Visible )
	{
	mVisible = Visible;
	}